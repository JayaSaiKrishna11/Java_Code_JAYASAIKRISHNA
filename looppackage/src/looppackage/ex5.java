package looppackage;

// Program  to demonstrate the different loops
public class ex5 {

	public static void main(String args[]) {
		
		for (int i = 1; i<=10; i++) {
			
			System.out.println(i);
		}
		
		int j = 11;
		while(j<=20) {
			
			System.out.println(j);
			j++;
		}
		
		int k = 21;
		do {
			System.out.println(k);
			k++;
			
		}while(k<=30);
		
	}
	
	
}
